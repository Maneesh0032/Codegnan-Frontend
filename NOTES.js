//what is dom in javascript?
// The Document Object Model (DOM) in JavaScript is a programming interface 
// that allows developers to interact with and manipulate the structure, style, 
// and content of a web page.
// The DOM represents the web page as a tree-like structure, where each element
// (such as headings, paragraphs, images, etc.) is represented as a node in the tree.
// JavaScript can be used to access and modify these nodes, allowing developers
// to dynamically change the content and appearance of a web page in response 
// to user interactions or other events.

// types in dom
// 1. Element Nodes: These represent HTML elements in the DOM tree. 
// They can be accessed and manipulated using JavaScript to change their attributes,
//  styles, and content.

// 2. Text Nodes: These represent the text content within HTML elements. 
// They can be accessed and modified to change the text displayed on a web page.

// 3. Attribute Nodes: These represent the attributes of HTML elements, 
// such as `id`, `class`, `src`, etc. They can be accessed and modified to 
// change the behavior or appearance of elements.

// 4. Comment Nodes: These represent comments in the HTML code. They are
//  not displayed on the web page but can be accessed and modified using JavaScript.

// 5. Document Node: This represents the entire HTML document and 
// serves as the root of the DOM tree. It provides methods for accessing and manipulating all other nodes in the DOM.


