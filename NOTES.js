// what is  and types of media query?
// Media queries are a powerful tool in responsive web design that allow developers to apply different styles based on the characteristics of the user's device. They enable websites to adapt and respond to various screen sizes, orientations, and resolutions. The main types of media queries include:
// 1. Width-based Media Queries: These media queries target specific screen widths. For example, `@media (max-width: 600px)` applies styles to devices with a screen width of 600 pixels or less.
// 2. Height-based Media Queries: These media queries target specific screen heights. For example, `@media (min-height: 800px)` applies styles to devices with a screen height of 800 pixels or more.
// 3. Orientation-based Media Queries: These media queries target the orientation of the device. For example, `@media (orientation: portrait)` applies styles to devices in portrait mode, while `@media (orientation: landscape)` applies styles to devices in landscape mode.
// 4. Resolution-based Media Queries: These media queries target the resolution of the device's screen. For example, `@media (min-resolution: 300dpi)` applies styles to devices with a resolution of 300 dots per inch or higher.
// 5. Aspect Ratio-based Media Queries: These media queries target the aspect ratio of the device's screen. For example, `@media (aspect-ratio: 16/9)` applies styles to devices with a 16:9 aspect ratio.
// 6. Color-based Media Queries: These media queries target the color capabilities of the device. For example, `@media (color)` applies styles to devices that support color, while `@media (monochrome)` applies styles to devices that only support monochrome displays.
// 7. Pointer-based Media Queries: These media queries target the type of input device used by the user. For example, `@media (pointer: coarse)` applies styles to devices with a coarse pointer (like a finger), while `@media (pointer: fine)` applies styles to devices with a fine pointer (like a mouse).

